package com.raelity.jvi.lib;

import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

import javax.swing.event.ChangeEvent;
import javax.swing.event.ChangeListener;
import javax.swing.event.EventListenerList;

/**
 *
 * @author Ernie Rael <err at raelity.com>
 */
public class MyEventListenerList extends EventListenerList
{
    // Notify all listeners that have registered interest for
    // notification on this event type.  The event instance
    // is lazily created using the parameters passed into
    // the fire method.

    public void fire(Class listenerClass, ChangeEvent ce) {
        // Guaranteed to return a non-null array
        Object[] listeners = getListenerList();
        // Process the listeners last to first, notifying
        // those that are interested in this event
        for (int i = listeners.length-2; i>=0; i-=2) {
            if (listeners[i]==listenerClass) {
                ((ChangeListener)listeners[i+1]).stateChanged(ce);
            }
        }
    }

    public void fire(Class listenerClass, ActionEvent ae) {
        // Guaranteed to return a non-null array
        Object[] listeners = getListenerList();
        // Process the listeners last to first, notifying
        // those that are interested in this event
        for (int i = listeners.length-2; i>=0; i-=2) {
            if (listeners[i]==listenerClass) {
                ((ActionListener)listeners[i+1]).actionPerformed(ae);
            }
        }
    }
}
